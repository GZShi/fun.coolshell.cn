// 使用这个程序将 BrainFuck 的代码转换成 c 代码


function compile(src) {
	src = src.replace(/\+/g, '++*ptr;\n');
	src = src.replace(/\-/g, '--*ptr;\n');
	src = src.replace(/\>/g, '++ptr;\n');
	src = src.replace(/\</g, '--ptr;\n');
	src = src.replace(/\./g, 'putchar(*ptr);\n');
	src = src.replace(/\,/g, '*ptr = getchar();\n');
	src = src.replace(/\[/g, 'while(*ptr) {\n');
	src = src.replace(/\]/g, '}');

	return ['',
		'int main() {',
		'  char buffer[256] = {0};',
		'  char *ptr = buffer;',
		'  ' + src,
		'  return 0;',
		'}'].join('\n');
}

